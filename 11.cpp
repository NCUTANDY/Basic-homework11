#include <stdio.h>
#include <stdlib.h>
int main(void)
{
    int num1,num2,num3,temp; 

	printf("請輸入三個數字,會進行排序：");  

	scanf("%d%d%d",&num1,&num2,&num3); 

	if(num1>num2)
	{ 

		temp=num1;  

		num1=num2; 

		num2=temp; 

	}

	if(num1>num3){ 

		temp=num1; 

		num1=num3; 

		num3=temp; 

	}

	if(num2>num3)
	{   

		temp=num2;   

		num2=num3; 

		num3=temp;

	}

	printf("從小到大的排列是:%d %d %d\n",num1,num2,num3);

	system("pause");
	
	return 0;
}
